// Last element removed by the pop()
// Number 

const a=[10,20,30,40];

console.log(a);

let b=(a.pop());

console.log(a);

console.log(b);


// String

const x=["bus","car","auto","bike","cycle"];

console.log(x);

let y=(x.pop());

console.log(x);

console.log(y);

// Negative numbers 

const u=[-3,4,-6,0,0,100];

console.log(u);

let v=(u.pop());

console.log(u);

console.log(v);